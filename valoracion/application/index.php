            function envia_mensaje(e){

                url = $("#form_contacto").attr("action");           
                data_send = $("#form_contacto").serialize();            
                $.ajax({
                        url : url, 
                        type: "POST",
                        data : data_send, 
                        beforeSend: function(){
                            console.log("enviando... ");            
                            $(".place_mensaje_usuario").html("Enviando ....");
                        }

                    }).done(function(data){
                        
                        console.log(data);          

                        $(".place_mensaje_usuario").html("Mensaje enviado!");
                        
                        document.getElementById("form_contacto").reset();

                    }).fail(function(){
                        console.log("Error al enviar  enviando... ");                   
                        $(".place_mensaje_usuario").html("Error al enviar el mensaje ....");
                    });

                e.preventDefault();
            }
            
